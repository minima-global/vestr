const initializeMinima = () => {
  MDS.DEBUG_HOST = "127.0.0.1";
  MDS.DEBUG_PORT = 9003;
  MDS.DEBUG_MINIDAPPID =
    "0x6EA015CA6AD12F5E38AC2C8BD1F2BEBB322902E09791D326AEAC2363CE2A840ECB32218ECF4F9277FF11E0F5B461A89B1D287FF67824FD430B17A46224746EE338DBB1A647299497F6C1F77288282476DF4C5F5CC210508034D0D1982E1FB061647D7DDA67EEABC63694C896C9A75BEF38120B8E24D15CB6D1831D9E28D5F048";

  MDS.init(
    (
      nodeEvent:
        | InitResponse
        | MiningResponse
        | NewBlockResponse
        | MinimaLogResponse
        | NewBalanceResponse
        | MaximaResponse
        | MDSTimerResponse
        | MaximaHosts
    ) => {
      switch (nodeEvent.event) {
        case "inited":
          // will have to dispatch from here..
          whenInit();
          break;
        case "NEWBLOCK":
          const newBlockData = nodeEvent.data;
          whenNewBlock(newBlockData);
          break;
        case "MINING":
          const miningData = nodeEvent.data;
          whenMining(miningData);
          break;
        case "MAXIMA":
          const maximaData = nodeEvent.data;
          whenMaxima(maximaData);
          break;
        case "NEWBALANCE":
          const newBalanceData = nodeEvent.data;

          whenNewBalance(newBalanceData);
          break;
        case "MINIMALOG":
          const minimaLogeData = nodeEvent.data;
          whenMinimaLog(minimaLogeData);
          break;
        case "MDS_TIMER_10SECONDS":
          const mdstimerdata = nodeEvent.data;
          whenMDSTimer(mdstimerdata);
          break;
        case "MAXIMAHOSTS":
          break;
        default:
        // console.error("Unknown event type: ", nodeEvent);
      }
    }
  );
};

